#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2 || (*s1 != *s2 && n))
		return (0);
	while (n - 1 && *s1 == *s2 && *s1)
	{
		s1 += 1;
		s2 += 1;
		n--;
		if (*s1 != *s2)
			return (0);
	}
	return (1);
}
